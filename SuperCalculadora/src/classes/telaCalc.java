    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        
        
        
        int n = Integer.parseInt(txtNum.getValue().toString());
        int resto = n % 2;
        lblResto.setText(Integer.toString(resto));
        double cubo = Math.pow(n, 3);
        lblCubo.setText(Double.toString(cubo)); 
        //para formatar em duas casas decimais (String.formst("%.2f", cubo))
        
        double raizq = Math.sqrt(n);
        lblRaizQ.setText(Double.toString(raizq));
        double raizc = Math.cbrt(n);
        lblRaizC.setText(Double.toString(raizc));
        int absoluto = Math.abs(n);
        lblAbs.setText(Integer.toString(absoluto));
        
        
        lblResto.setVisible (true);
        lblCubo.setVisible (true);
        lblRaizQ.setVisible (true);
        lblRaizC.setVisible (true);
        lblAbs.setVisible (true);
        lbl1.setVisible (true);
        lbl2.setVisible (true);
        lbl3.setVisible (true);
        lbl4.setVisible (true);
        lbl5.setVisible (true);
        
    }//GEN-LAST:event_btnCalcActionPerformed

